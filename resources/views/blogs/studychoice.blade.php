              <h2>Study choice</h2>
              <h3>Waarom ICT de juiste studie voor mij is</h3>
              <p>ICT staat voor mij centraal als de ideale mix van theorie en praktijk. Door mijn eerdere ervaring met softwareontwikkeling, werd ik geïnspireerd om mijn kennis uit te breiden, met een specifieke focus op data-analyse en AI. ICT is niet alleen een boeiende technische discipline, maar ook een sleutel tot het oplossen van maatschappelijke problemen. Mijn doel is om met behulp van technologie bij te dragen aan vraagstukken zoals zorg en duurzaamheid.</p>

              <h3>Studiekeuze activiteiten</h3>
              <p>Mijn keuze voor ICT is gevormd door diverse studieactiviteiten. Ik heb hands-on ervaring opgedaan in zowel front-end als back-end softwareontwikkeling, wat me een volledig beeld gaf van hoe digitale oplossingen worden ontwikkeld. Deelname aan hackathons en workshops op het gebied van AI en data science heeft mijn interesse in deze vakgebieden versterkt, vooral in toepassingen voor de gezondheidszorg en duurzaamheid.</p>

              <h3>Motiveer je studiekeuze</h3>
              <p>Vandaag de dag gaat technologie verder dan alleen efficiëntie; het biedt oplossingen voor grootschalige problemen. Ik wil mijn technische kennis inzetten om innovatieve oplossingen te ontwikkelen die een directe impact hebben op de maatschappij. Of het nu gaat om betere zorg of milieuvriendelijke technologie, ik geloof dat ICT het gereedschap is dat deze doelen bereikbaar maakt.</p>

              <h3>Na het voltooien van de studie</h3>
              <p>Na mijn studie wil ik me specialiseren in domeinen waar ik een tastbare bijdrage kan leveren aan de maatschappij. In de toekomst zie ik mezelf werken aan projecten die betrekking hebben op gezondheidstechnologie of duurzaamheid. Door gebruik te maken van data-analyse en AI wil ik systemen ontwikkelen die de levenskwaliteit verbeteren en tegelijkertijd de impact op het milieu minimaliseren.</p>

              <p>Mijn ambitie is om door middel van ICT de wereld een beetje beter te maken, door het creëren van innovatieve en duurzame oplossingen die echt het verschil maken.</p>
